package sistematutoriasfx.modelo.pojo;

public class Rol {
    private int idRol;
    private String nombre;

    public Rol() {
    }

    public Rol(int idRol, String nombre) {
        this.idRol = idRol;
        this.nombre = nombre;
    }

    public int getIdRol() {
        return idRol;
    }

    public void setIdRol(int idRol) {
        this.idRol = idRol;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    
    // Este toString es vital para llenar los ComboBox en la interfaz m√°s adelante
    @Override
    public String toString() {
        return nombre;
    }
}